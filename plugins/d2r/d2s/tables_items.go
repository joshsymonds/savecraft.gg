// getItemTypeID returns the type ID for an item based on its code.
func getItemTypeID(code string) uint64 {
	if _, ok := armorCodes[code]; ok {
		return itemTypeArmor
	}
	if _, ok := shieldCodes[code]; ok {
		return itemTypeShield
	}
	if _, ok := weaponCodes[code]; ok {
		return itemTypeWeapon
	}
	return itemTypeOther
}

// getItemTypeName returns the human-readable name for an item given its code
// and pre-resolved type ID.
func getItemTypeName(code string, typeID uint64) string {
	switch typeID {
	case itemTypeArmor:
		return armorCodes[code]
	case itemTypeShield:
		return shieldCodes[code]
	case itemTypeWeapon:
		return weaponCodes[code]
	default:
		return miscCodes[code]
	}
}
